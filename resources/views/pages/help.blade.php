                                        <h2>4. 常见问题</h2>
                                        <div id="help-4-1">
                                            <h4>4.1 你们怎么既有“原创小说”版面，又有“文库”版面，这么混乱的啊？！</h4>
                                            <em><a href="http://www.xys.org/bbcode.html">参考资料：网络上关于BBCode的介绍</a></em>
                                            <p>BBCode，这是网站目前使用的文字格式系统（虽然，是一个魔改版）。如果您的帖子需要”斜体“、”加粗“、高亮、列表、插图、插入超链接...等等格式，您可以通过点勾“显示编辑器”使得编辑器出现，并在对应的文本框里点击按钮应用格式，再点击“预览”查看应用效果。原始BBcode不识别普通空行，但本站是识别的。<br> 此外，本站会清理除<code>&lt;br&gt;</code>之外的、段落间所有的冗余空行。简单地说，作者如果希望实现“多空行”的显示效果，将<code>&lt;br&gt;</code>插入到需要空行的地方即可，插入多少个，就多空几行。</p>
                                        </div>
                                        <div id="help-4-2">
                                            <h4>4.2 听说这里的编辑器识别的是BBCode，什么是BBCode，怎么使用？</h4>
                                            <em><a href="http://www.xys.org/bbcode.html">参考资料：网络上关于BBCode的介绍</a></em>
                                            <p>BBCode，这是网站目前使用的文字格式系统（虽然，是一个魔改版）。如果您的帖子需要”斜体“、”加粗“、高亮、列表、插图、插入超链接...等等格式，您可以通过点勾“显示编辑器”使得编辑器出现，并在对应的文本框里点击按钮应用格式，再点击“预览”查看应用效果。原始BBcode不识别普通空行，但本站是识别的。<br> 此外，本站会清理除<code>&lt;br&gt;</code>之外的、段落间所有的冗余空行。简单地说，作者如果希望实现“多空行”的显示效果，将<code>&lt;br&gt;</code>插入到需要空行的地方即可，插入多少个，就多空几行。</p>
                                        </div>
                                        <code>注意，编辑器正在施工中，如果出现显示问题，请稍安勿躁，前往版务区<em><a href="http://sosad.fun/threads/49">跟帖</a></em> 呼唤管理员解决问题。</code>
