                                            <p>主题贴收藏：主题贴收藏和文章收藏操作一致，点击“收藏”即可。主题贴收藏后，会提示其他人对这一主题进行<code>讨论回帖</code>的情况。</p>
                                            <p>动态收藏：这里可以查看您所关注用户的小微博更新。</p>
