                                        <div id="help-1-2">
                                            <h4>1.2 积分，咸鱼，剩饭，丧点</h4>
                                            <p>以上是本站特色的虚拟积分系统。发文、发帖、回帖、点评、签到、参加活动……都能增加这些积分。积分影响等级，等级影响到网站一小部分的功能使用，比如发送私信等。咸鱼和剩饭能够对作者进行奖励。本站大部分功能对全部用户开放，不需要对积分获取过于执着。</p>
                                            <em><a href="#help-2-4">(前往2.4中看文互动，明确这些虚拟货币的用途)</a></em>
