                                        <h2>2. 读者看文</h2>
                                        <div id="help-2-1">
                                            <h4>2.1 文库淘文：怎么只看我想看的类型/性向/进度？</h4>
                                            <p><code>a. 单项淘文：</code>在文库去，点击任何关于“书籍进度（连载/完结/暂停）”，“书籍篇幅（短篇中篇长篇）”，“书籍原创性（原创/同人）”，“书籍性向（BL/GL/BG/GB/无CP/混合性向/其他性向）”，“书籍类型（科幻/奇幻/现代/古代...）”，“书籍标签（美强/强强/正剧/小甜饼/暗黑/HE...）”以上任何超链接，点击之后，都可以跳转进入筛选页面。浏览器收藏该页面，下次就可以直接淘这类文章。</p>
                                            <p><code>b. 复合淘文（积极排除不想看的类型）：</code>点击导航栏“文库”按钮即可进入文库淘文。就在导航栏之下，有筛选功能。这里提供复合筛选，并且一键进入该筛选特定的网页。读者可以收藏筛选结果所在的网页，日后直接一键进入淘文。</p>
                                            <p>关于文库排序：目前文库按照最后更新章节时间倒序排列（此章节必须达到一定字数要求，数据见本页最下的“实时数据”展示）。以后会推出更多排序方式。</p>
                                        </div>
                                        <br>
