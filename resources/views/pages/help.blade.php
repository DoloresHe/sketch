                                        <h2>3 作者发文</h2>
                                        <div id="help-3-1">
                                            <h4>3.1 发文入口</h4>
                                            <p>从右上角“用户专区->我要发文“进入，按顺序点选填写相应信息即可发文。</p>
                                            <p>发文时如有延迟，请勿`反复`点选“发布”键。</p>
                                            <p>请区分`文案`和`正文`，正文请点击`发新章节`进行发布，不要放在文案内。标题和文案请符合规范。</p>
                                        </div>
                                        <br>
                                        <div id="help-3-2">
                                            <h4>3.2 主题贴能做的操作</h4>
                                            <p>是否公开本主题贴：如果不勾选，则帖子不对外开放，只能从个人主页进入，可以作为<code>私下存文</code>的渠道。管理员发现帖子标题等处不规范，也会将该项取消勾选（也就是隐藏处理），作者修改好后可以自行公开，恢复帖子的阅读。</p>
                                            <p>是否允许跟帖：如果不勾选，其他读者只能看文不能评论。如果遇到文下争议，来不及等待管理员处理，此操作可以应急。（之后可以悠哉悠哉地去管理版面登记，举报不友好评论，惩罚ky分子）</p>
                                            <p>是否允许讨论帖下载：勾选后，其他读者能够以支付作者咸鱼和剩饭为代价，实时下载包含书评在内、按时间顺序排列的全文txt文件作为存档。本选项默认勾选。</p>
                                            <p>是否允许书籍下载：勾选后，高等级读者能够以支付作者<em>更多</em>咸鱼和剩饭为代价，实时下载不包含书评在内，只含章节正文的纯净书籍txt文件。本选项默认不勾选。</p>
                                        </div>
