                                            <p>激活： 由于邮件服务不足，邮件激活功能暂时关闭，目前不需手工激活，注册即可登陆</p>
                                            <p>忘记密码： 在登陆页面点选“忘记密码”，输入自己的邮箱，即可接收重置密码邮件，点开其中的链接重置密码</p>
                                            <p>其他疑难，可以在<em><a href="http://sosad.fun/threads/49">版务管理专版</a></em>跟帖，或者微博站长@文栈君咨询。</p>
                                        </div>
