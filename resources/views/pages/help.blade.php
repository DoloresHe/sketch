                                        <div id="help-4-3">
                                            <h4>4.3 什么是markdown格式，怎么使用？</h4>
                                            <p>这是网站目前不再推荐使用的格式系统（虽然，是一个魔改版）。请注意，markdown语法要求两个空格或者两个回车作为换行符号，但本站处理成普通回车即可换行，并且会清理除<code>&lt;br&gt;</code>之外的、段落间所有的冗余空行。如果对此语法没有深入了解的兴趣，请千万不要勾选本项。</p>
                                        </div>
                                        <br>
                                        <div id="help-4-4">
                                            <h4>4.4 页面崩溃，丢失数据怎么办？急，在线等！</h4>
                                            <p>用户登录后，所有大文本框下均有<code>”恢复数据“</code>按键，是随时向服务器提交您之前输入的数据的，点选它即可恢复上次输入的文本文件。<code>如果不小心误按，再点一次即可撤销。</code></p>
                                        </div>
                                        <br>
                                        <div id="help-4-5">
                                            <h4>4.5 什么是段首缩进，怎么操作？</h4>
                                            <p>勾选之后能够在现有文字基础上自动在段首<code>空两格</code>。不选的话，就不自动空格（缩进）。默认是勾选的，如果作者的文字本身已有空格，可以取消勾选此项，获得更好看的显示效果。</p>
                                        </div>
